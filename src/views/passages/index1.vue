        维基百科上對這個單詞的定义如下：Critical thinking consists of a mental process of analyzing or evaluating information, particularly statements or propositions that people have offered as true. It forms a process of reflecting upon the meaning of statements, examining the offered evidence and reasoning, and forming judgments about the facts.
