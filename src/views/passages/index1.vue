      <h2>2 Deductive Reasoning</h2>
      <p>当你坐下来写一篇文章时，善用演绎推理方法。你可能不知道在计划或撰写论文的过程中如何使用演绎推理。純粹的演绎推理其實就是通过单一的思路从推理中得出具体的结论。这樣很容易合理的理解不同的的想法並加以分析。舉一個例子：
        在統計學的角度上看，就是俗稱的“集合”
        <img :src="P1Img5"><br>
        假設每一隻狗都有犬齒，而Tuffy是一個狗，我們可以推理出Tuffy犬齒
        這一段話就是類似你的Eassy裡，你必须利用类似的推论来做出具体的陈述和结论。

        再舉個例子，在一篇全球气候变化的文章中，研究气候变化和粮食安全的影响。
        我們可以使用演绎推理的一个例子所下：<br>
        <img :src="P1Img6"><br>
        如果气候变化导致干旱，干旱影响农业，那么气候变化将影响人類获得食物的机会。
        当然，善用歷史曾經發生的事件来支持主张，确保文章引用清晰、合乎逻辑得出最後的结论。如此一來你的導師也會給你一個滿意的成績。
      </p>

      <h2>3 Critical Analysis</h2>
      <p>
        批判性分析是分析材料并对其进行基本判断或意见的能力。每当我們看新聞或是媒體想法时，批判性分析是一個學術人必備的能力。论文中常见的批判性分析可以分為：
        深度阅读
        深度阅读主要用于文学批评，是对文学文本——小说、短篇小说、诗歌等——的象征意义、隐喻、人物和情节的深刻探討。深度阅读也是对历史、传记和政治背景的探索，在这些背景下，文学文本可以幫助理解其中的歷史背景。例如威廉莎士比亚喜剧中的性别政治，當我們深刻的研究他对语言的使用、诙谐的对话便可以窺探一二。

        实证分析
        实证分析通过实验者的经验或观察来研究病例，可以测试新药对一组不同情況且受控患者的影响。实证分析在解决问题的论文中特别有用，可以支持甚至驳斥特定问题的解决方案。例如，作者可能会分析关于犯罪行为的实证研究，以得出关于法律、政策、贫困或社会影响可能如何影响该行为的更深入的结论，然后建议改变政策以解决正在研究的问题。這一點對學生來說比較困難，因為涉及的經費比較多,但這樣往往可以拿到更高的分數。
      </p>

      <h2>4 Evaluation</h2>
      <p>
        评估是批判性思维中极其重要的技能。当你提出一个论点时，你必须用事实和例子来支持它，但你的论点只与你提供的信息一样重量時。这意味着评估使用的陈述、意见、事实和数据等等是否有效且符合逻辑。一但使用的材料帶有偏见，那這篇文章就不是個合格的文章。
        例如，声称同性婚姻将导致儿童性行为或兽交合法化的声明既不合逻辑也不合理，這句陳述就帶有偏见。
        <img :src="P1Img7">
        此外如果有最新文獻或是數據來源，建議優先使用。尤其是在涉及科学或医学评论和期刊时。D哥團隊強烈建議使用的书目材料中至少有一半应该在五到八年内。这样，您将更好地了解最新最全面的資訊、同時避免使用不确定的内容。這時你提出的论点说服力便強上不少。
      </p>

      <h2>5 Problem Solving</h2>
      <p>
        解决问题是另一种批判性思维技能。假设你發現在文章无法连贯地组织你想表達的觀點。这是留學生寫論文時经常遇到的问题，建議通过拆解和评估來找出可能的原因，比如說主题是否太宽泛？一篇只有五個Part的文章中是否包含太多的想法？这幾個想法是否都大道已經可以成为论文主题？
        舉個例子，如果文科學生广泛探讨莎士比亚如何描写女性，作者可能会将其缩小到Macbeth夫人等人物以及她们的权力地位如何反映伊丽莎白时代社会的性别政治。通过猜測和分解问题。这也是寫作中批判性思维的本质之一。
      </p>

