        取捨是批判性思维中的一项重要技能，因为它要求您在茫茫大海裡那麼多的數據文獻當中决定哪个选择是最合適或最有用的。当我們选择好了主题和论文陈述、组织论文、进行研究并确定哪些信息關聯时，取捨的重要性就凸顯出來了。
        Decision Making也意味着选择最好的方式来论证你的意见。毕竟，不连贯的意见与根本没有關聯的數據就意味著Fail。例如，當你和導師討論後決定写一篇关于政治伦理相關的文章。而你清楚知道導師的領域是偏财务相關，那你文章的題目可以怎麼定？
        同時考慮到後面的论证性、问题解决方案、说服性、说明性? 每种类型的论文都能以不同形式呈现，但到底要如何掌握主题，貼近導師的領域才是一個最良好的开端。这將幫助你100%獲得老師的支持和友善的評分。
